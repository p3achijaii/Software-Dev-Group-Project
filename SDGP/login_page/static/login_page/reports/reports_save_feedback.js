(() => {
    "use strict";

    function getActiveButtonLabel(selector, fallbackLabel) {
        const activeButton = document.querySelector(selector);
        if (!activeButton) return fallbackLabel;
        return activeButton.textContent.replace(/\s+/g, " ").trim() || fallbackLabel;
    }

    function getActiveDocType() {
        const activeButton = document.querySelector(".report-doc-type-button.Option_selector_active");
        const rawDocType = activeButton?.dataset.docType || "pdf";
        if (rawDocType === "excel" || rawDocType === "xlsx") return "xlsx";
        return "pdf";
    }

    function getActiveReportNode() {
        const activeDocPanel = document.querySelector(
            ".report-doc-panel.report-doc-panel-active[aria-hidden='false']"
        );
        if (!activeDocPanel) return null;

        return activeDocPanel.querySelector(
            ".pdf-template-page.pdf-template-page-active[aria-hidden='false'], .excel-template-page.excel-template-page-active[aria-hidden='false']"
        );
    }

    function readTextOrValue(element) {
        if (!element) return "";
        if (element.tagName === "TEXTAREA" || element.tagName === "INPUT") {
            return (element.value || "").trim();
        }
        return (element.textContent || "").replace(/\s+/g, " ").trim();
    }

    function buildReportPayload(reportNode, reportType) {
        const titleNode = reportNode.querySelector(".Doc_title");
        const titleValue = readTextOrValue(titleNode) || reportType;
        const meta = [];
        const metaDateNode = reportNode.querySelector("[data-meta-date]");
        const metaTimeNode = reportNode.querySelector("[data-meta-time]");
        if (metaDateNode) {
            const parts = readTextOrValue(metaDateNode).split(":");
            const value = parts.length > 1 ? parts.slice(1).join(":").trim() : readTextOrValue(metaDateNode);
            meta.push({ label: "Generation Date", value });
        }
        if (metaTimeNode) {
            const parts = readTextOrValue(metaTimeNode).split(":");
            const value = parts.length > 1 ? parts.slice(1).join(":").trim() : readTextOrValue(metaTimeNode);
            meta.push({ label: "Generation Time", value });
        }
        const generatedByNode = Array.from(reportNode.querySelectorAll(".generated_text")).find(
            (node) => /Generated by/i.test(node.textContent || "")
        );
        if (generatedByNode) {
            const parts = readTextOrValue(generatedByNode).split(":");
            meta.push({
                label: "Generated by",
                value: parts.length > 1 ? parts.slice(1).join(":").trim() : ""
            });
        }

        const sections = [];
        const disclaimerNode = reportNode.querySelector(".disclaimer");
        if (disclaimerNode) {
            sections.push({
                heading: "Overview",
                paragraph: readTextOrValue(disclaimerNode),
                rows: []
            });
        }

        const fillingTextNodes = Array.from(reportNode.querySelectorAll(".Filling_text"));
        fillingTextNodes.forEach((node, index) => {
            sections.push({
                heading: index === 0 ? "Findings" : `Findings (${index + 1})`,
                paragraph: readTextOrValue(node),
                rows: []
            });
        });

        const userInputNodes = Array.from(reportNode.querySelectorAll("textarea"));
        userInputNodes.forEach((node) => {
            const value = (node.value || "").trim();
            if (value) {
                sections.push({
                    heading: "Your input",
                    paragraph: value,
                    rows: []
                });
            }
        });

        const signatureNode = reportNode.querySelector(".Signature");
        if (signatureNode) {
            sections.push({
                heading: "",
                paragraph: readTextOrValue(signatureNode),
                rows: []
            });
        }

        return {
            title: titleValue,
            subtitle: reportType,
            meta,
            sections
        };
    }

    function showToast(toastNode, hideTimeoutBox, message) {
        toastNode.textContent = message;
        toastNode.classList.add("is-visible");
        if (hideTimeoutBox.id) {
            window.clearTimeout(hideTimeoutBox.id);
        }
        hideTimeoutBox.id = window.setTimeout(() => {
            toastNode.classList.remove("is-visible");
        }, 2600);
    }

    async function saveActiveReport(toastNode, hideTimeoutBox) {
        const activeReportNode = getActiveReportNode();
        if (!activeReportNode) {
            showToast(toastNode, hideTimeoutBox, "Choose a report before saving.");
            return;
        }

        const reportType = getActiveButtonLabel(
            ".report-type-button.Option_selector_active",
            "Report"
        );
        const docType = getActiveDocType();
        const payload = buildReportPayload(activeReportNode, reportType);

        try {
            const csrfToken = (window.SDGP_CSRF && window.SDGP_CSRF.getCsrfToken()) || "";
            const response = await fetch("/user-home/tools/report/save/", {
                method: "POST",
                credentials: "same-origin",
                headers: {
                    "Content-Type": "application/json",
                    "X-CSRFToken": csrfToken
                },
                body: JSON.stringify({
                    title: payload.title,
                    report_type: reportType,
                    doc_type: docType,
                    payload
                })
            });

            if (!response.ok) {
                const errorBody = await response.json().catch(() => ({}));
                showToast(toastNode, hideTimeoutBox, errorBody.message || "Failed to save report.");
                return;
            }

            const result = await response.json();
            if (result.export_url) {
                const downloadLink = document.createElement("a");
                downloadLink.href = result.export_url;
                downloadLink.download = result.file_name || "";
                document.body.appendChild(downloadLink);
                downloadLink.click();
                downloadLink.remove();
            }
            showToast(toastNode, hideTimeoutBox, `Saved as ${result.file_name || "report"}.`);
        } catch (error) {
            console.warn("Reports save: failed.", error);
            showToast(toastNode, hideTimeoutBox, "Failed to save report.");
        }
    }

    function initSaveFeedback() {
        const saveButton = document.querySelector("#Button_Save .Save_btn");
        if (!saveButton) return;

        const toastNode = document.createElement("div");
        toastNode.className = "save-toast";
        toastNode.setAttribute("role", "status");
        toastNode.setAttribute("aria-live", "polite");
        toastNode.setAttribute("aria-atomic", "true");
        document.body.appendChild(toastNode);

        const hideTimeoutBox = { id: 0 };

        saveButton.addEventListener("click", () => {
            saveActiveReport(toastNode, hideTimeoutBox);
        });
    }

    document.addEventListener("DOMContentLoaded", initSaveFeedback);
})();
